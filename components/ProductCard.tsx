"use client";

import { assets } from "@/assets/assets";
import { useAppContext } from "@/context/AppContext";
import Image from "next/image";

export default function ProductCard({ product }) {
  const { currency, router } = useAppContext();
  return (
    <div
      className="flex flex-col items-start gap-0.5 w-full cursor-pointer"
      onClick={() => {
        router.push("/product/" + product._id);
        scrollTo(0, 0);
      }}
    >
      <div className="cursor-pointer group relative bg-gray-500/10 rounded-lg w-full flex items-center justify-center">
        <Image
          src={product.image[0]}
          alt={product.name}
          width={800}
          height={800}
          className="group-hover:scale-105 transition object-cover w-full h-full"
        />
        <button className="absolute top-2 right-2 p-2 bg-white rounded-full shadow-md">
          <Image
            src={assets.heart_icon}
            alt="Heart Icon"
            width={12}
            height={12}
            className="w-3 h-3"
          />
        </button>
      </div>
      <h6 className="md:text-base font-medium pt-2 w-full truncate">
        {product.name}
      </h6>
      <p className="w-full text-xs text-gray-500/70 max-sm:hidden truncate">
        {product.description}
      </p>
      <div className="flex items-center gap-2">
        <p className="text-xs">{4.5}</p>
        <div className="flex items-center gap-0.5">
          {Array.from({ length: 5 }).map((_, index) => {
            return (
              <Image
                src={
                  index < Math.floor(4)
                    ? assets.star_icon
                    : assets.star_dull_icon
                }
                width={12}
                height={12}
                alt="Stars"
                key={index}
              />
            );
          })}
        </div>
      </div>

      <div className="flex items-end justify-between w-full mt-1">
        <p className="text-base font-medium">
          {currency}
          {product.offerPrice}
        </p>
        <button className="max-sm:hidden px-4 py-1.5 text-gray-500 border border-gray-500/20 rounded-full text-xs hover:bg-slate-50 transition">
          Buy now
        </button>
      </div>
    </div>
  );
}
